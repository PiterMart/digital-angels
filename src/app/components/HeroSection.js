export default function HeroSection() {
  const [consoleLines, setConsoleLines] = useState([]);
  const consoleRef = useRef(null);
  const [isComplete, setIsComplete] = useState(false);

  // Animate console logs - run only once
  useEffect(() => {
    if (isComplete) return;
    
    const lines = consoleMessage.trim().split('\n');
    let currentIndex = 0;
    
    const interval = setInterval(() => {
      if (currentIndex < lines.length) {
        const nextLine = lines[currentIndex];
        // Add empty line for better spacing in ASCII art sections
        const isAsciiLine = nextLine.includes('#') || nextLine.includes('@') || nextLine.includes('&') || nextLine.includes('B') || nextLine.includes('G') || nextLine.includes('P') || nextLine.includes('Y') || nextLine.includes('J') || nextLine.includes('7') || nextLine.includes('5');
        setConsoleLines(prev => [...prev, { type: 'log', text: nextLine }]);
        currentIndex++;
      } else {
        setIsComplete(true);
        clearInterval(interval);
      }
    }, 80); // Slightly faster for better effect

    return () => clearInterval(interval);
  }, [isComplete]);

  // Scroll console to bottom
  useEffect(() => {
    if (consoleRef.current) {
      consoleRef.current.scrollTop = consoleRef.current.scrollHeight;
    }
  }, [consoleLines]);

  return (
    <div className={styles.hero}>
      <div className={styles.consoleLogs} ref={consoleRef}>
        {consoleLines.map((line, index) => (
          <p key={index} className={styles.consoleLine}>
            {line?.text || ''}
          </p>
        ))}
      </div>
    </div>
  );
}